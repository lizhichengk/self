  <div id="app">
    <img alt="Vue logo" src="./assets/logo.png">
    <router-view></router-view>
  </div>
</template>

<script>
export default {
  name: 'app',
  data() {
    return {
      num:0
    };
  },
  created() {
      sessionStorage.setItem('num', 0);
      setTimeout(function () {
        this.num = 1;
        sessionStorage.setItem('num',this.num);
      }, 3000);
  }
};
</script>

